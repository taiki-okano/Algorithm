#include <iostream>
#include <algorithm>
#include <string>

constexpr int MAX_NL = 100;

int main(){
    int N, L;
    std::string str[MAX_NL];
    std::cin >> N >> L;
    for(int i = 0; i < N; ++i){
        std::cin >> str[i];
    }
    std::sort(str, str + N);
    for(int i = 0; i < N; ++i){
        std::cout << str[i];
    }
    std::cout << std::endl;
}
